//////////////////////////////////////////////////////
// INCLUDES
//////////////////////////////////////////////////////
const express = require('express'); // require the express framework

//////////////////////////////////////////////////////
// CREATE APP
//////////////////////////////////////////////////////
const app = express();   // make app as express

//////////////////////////////////////////////////////
// USES
//////////////////////////////////////////////////////
app.use(express.json()); //json
app.use(express.urlencoded({extended:false}));  // middleware


////////////////////////////////////////////////////////
// STATIC
////////////////////////////////////////////////////
app.use("/", express.static('public'));
// need this above to server html files



//////////////////////////////////////////////////////
// SETUP ROUTES
//////////////////////////////////////////////////////
const mainRoutes = require('./routes/mainRoutes'); // define main routes to use it
app.use("/", mainRoutes);
// need api to prevent conflicts with other routes


//////////////////////////////////////////////////////
// EXPORT APP
//////////////////////////////////////////////////////
module.exports = app;